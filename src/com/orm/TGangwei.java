package com.orm;

public class TGangwei
{
	private String id;
	private String mingcheng;
	private String qishi;
	private String jiezhi;
	private String neirong;
	private String baochou;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMingcheng() {
		return mingcheng;
	}
	public void setMingcheng(String mingcheng) {
		this.mingcheng = mingcheng;
	}
	public String getQishi() {
		return qishi;
	}
	public void setQishi(String qishi) {
		this.qishi = qishi;
	}
	public String getJiezhi() {
		return jiezhi;
	}
	public void setJiezhi(String jiezhi) {
		this.jiezhi = jiezhi;
	}
	public String getNeirong() {
		return neirong;
	}
	public void setNeirong(String neirong) {
		this.neirong = neirong;
	}
	public String getBaochou() {
		return baochou;
	}
	public void setBaochou(String baochou) {
		this.baochou = baochou;
	}
}
